package ejemplo01;

public class Alumno {
	//Atributos
	
	int edad;
	String nombre;
	String apellidos;
	double notaMedia;
	
	//Constructor
	public Alumno (int edad, String nombre, String apellidos, double notaMedia) {
		this.edad=edad;
		this.nombre=nombre;
		this.apellidos=apellidos;
		this.notaMedia=notaMedia;
	}
}
