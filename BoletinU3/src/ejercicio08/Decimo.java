package ejercicio08;

public class Decimo {
	//ATRIBUTOS
	private double precio;
	private int num;
	
	//CONSTRUCTOR
	public Decimo (double precio, int num) {
		super();
		this.precio = precio;
		this.num = num;
	}

	//GETTERS Y SETTERS	
	public double getPrecio() {
		return precio;
	}

	public void setPrecio(double precio) {
		this.precio = precio;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}
	
	//METODOS
	
	
}
